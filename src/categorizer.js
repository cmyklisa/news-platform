function fromUrl(url) {
  if (!url) return null;
  for (const [re, cat] of URL_RULES) if (re.test(url)) return cat;
  return null;
}

function scoreCat(text, cat) {
  let s = 0;
  for (const kw of (KW[cat] || [])) if (text.includes(kw)) s += kw.length >= 3 ? 2 : 1;
  if (s > 0 && SPECIFIC.has(cat)) s += 1;
  return s;
}

function categorize({ title = '', url = '', rssCategories = [] } = {}) {
  const fromHint = fromUrl(url);
  if (fromHint) return fromHint;
  const text = `${title} ${(rssCategories || []).join(' ')}`;
  let best = null, bestScore = 0;
  for (const cat of PRIORITY) {
    const s = scoreCat(text, cat);
    if (s > bestScore) { bestScore = s; best = cat; }
  }
  return bestScore >= 2 ? best : 'domestic';
}

